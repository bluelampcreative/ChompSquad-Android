package com.bluelampcreative.chompsquad.ui.theme

import androidx.compose.ui.unit.dp

/**
 * ChompSquad spacing token scale.
 *
 * Use these instead of raw dp literals so that rhythm is consistent across the app and easy to
 * adjust globally.
 *
 * xs → 4dp intra-component gaps, icon padding sm → 8dp tight groupings, chip padding md → 16dp
 * standard content margin / card padding lg → 24dp section separation xl → 32dp large section gaps,
 * hero padding xxl → 48dp screen-level vertical breathing room
 */
object ChompSpacing {
  val xs = 4.dp
  val sm = 8.dp
  val md = 16.dp
  val lg = 24.dp
  val xl = 32.dp
  val xxl = 48.dp
}
